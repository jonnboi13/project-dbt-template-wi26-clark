
  
    

        create or replace transient table SNOWBEARAIR_DB.RAW.fact_ml_analysis
         as
        (SELECT
    d.CALENDAR_DATE,
    s.SYMBOL,
    w.CITY,
    r.OPEN,
    r.HIGH,
    r.LOW,
    r.CLOSE,
    r.VOLUME,
    rw.MAX_TEMP,
    rw.PRECIP AS PRECIPITATION,
    (
        SELECT COUNT(*) 
        FROM SNOWBEARAIR_DB.RAW.NEWS_API_DAG_OLIPHANT_J n
        WHERE CAST(n.DATETIME AS DATE) = CAST(r.DATETIME AS DATE)
    ) AS NEWS_COUNT_TOTAL
FROM SNOWBEARAIR_DB.RAW.MONGO_DAG_OLIPHANT_J r
JOIN SNOWBEARAIR_DB.RAW.dim_date d 
    ON CAST(r.DATETIME AS DATE) = d.CALENDAR_DATE
JOIN SNOWBEARAIR_DB.RAW.dim_stocks s 
    ON r.SYMBOL = s.SYMBOL
LEFT JOIN SNOWBEARAIR_DB.RAW.WEATHER_API_DAG_OLIPHANT_J rw 
    ON CAST(r.DATETIME AS DATE) = rw.DATE
LEFT JOIN SNOWBEARAIR_DB.RAW.dim_weather_location w 
    ON rw.CITY = w.CITY
        );
      
  